/* 
Monte um programa para ler um valor e informar 
se o valor é maior do que 10. Caso o valor não 
seja, informe ‘valor menor ou igual’ 
*/
let valor = parseInt(prompt('Informe um valor'));
if (valor > 10) {
    alert('valor informado é maior do que 10');
} else {
    alert('valor informado é menor ou igual a 10');
}